async def handle_text(update: Update, context: ContextTypes.DEFAULT_TYPE):
    msg = update.effective_message
    chat = update.effective_chat
    user = update.effective_user
    if not msg or not user or not msg.text:
        return

    text = msg.text.lower()

    for key, answers in MURKA_REPLIES.items():
        if key in text:
            # üé∂ –ì–æ–ª–æ—Å–æ–≤—ã–µ
            if "audio" in answers:
                try:
                    audio_path = os.path.join(os.getcwd(), "song.ogg")
                    with open(audio_path, "rb") as audio:
                        await msg.reply_voice(audio)
                except Exception:
                    await msg.reply_text("–û–π, –ø–µ—Å–Ω—é –ø–æ—Ç–µ—Ä—è–ª–∞ üòø")

            # üé• –í–∏–¥–µ–æ-–∫—Ä—É–∂–∫–∏
            elif "video_note" in answers:
                try:
                    video_file = random.choice(["murkakup.mp4", "murkac.mp4"])
                    video_path = os.path.join(os.getcwd(), video_file)
                    with open(video_path, "rb") as video:
                        await msg.reply_video_note(video)
                except Exception:
                    await msg.reply_text("–û–π, –∫—Ä—É–∂–æ—á–µ–∫ –ø–æ—Ç–µ—Ä—è–ª–∞ üòø")

            # üé± –®–∞—Ä —Å—É–¥—å–±—ã
            elif "magic_ball" in answers:
                await msg.reply_text(random.choice(MAGIC_BALL))

            # üíñ –ö–æ–º–ø–ª–∏–º–µ–Ω—Ç—ã
            elif "compliment" in answers:
                await msg.reply_text(random.choice(COMPLIMENTS))

            # üå¶ –ü–æ–≥–æ–¥–∞
            elif "weather" in answers:
                await msg.reply_text(random.choice(WEATHER))

            # üî¢ –£–≥–∞–¥–∞–π —á–∏—Å–ª–æ
            elif "number" in answers:
                await msg.reply_text(f"–Ø –∑–∞–≥–∞–¥–∞–ª–∞ —á–∏—Å–ª–æ: {random.randint(1,10)} üî¢")

            # ü§ó –û–±–Ω–∏–º–∞—à–∫–∏
            elif "hug" in answers:
                await msg.reply_text("–ú—É—Ä–∫–∞ –æ–±–Ω—è–ª–∞ —Ç–µ–±—è –ª–∞–ø–∫–∞–º–∏ ü§óüêæ")

            # üé≤ –ö—É–±–∏–∫
            elif "dice" in answers:
                await msg.reply_dice()

            else:
                await msg.reply_text(random.choice(answers))
            return

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø–ª–æ—Ö–∏–µ —Å–ª–æ–≤–∞ (–æ—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å, –Ω–µ –º–µ–Ω—è–µ–º)
    if any(r.search(text) for r in BAD_REGEXES):
        key = (chat.id, user.id)
        now = time.time()
        q = violations[key]
        while q and now - q[0] > WINDOW_SECONDS:
            q.popleft()
        q.append(now)
        strikes = len(q)
        st = state[key]
        name = user.mention_html()

        if chat.type == ChatType.PRIVATE:
            if now - st.last_warn_at > 15:
                await msg.reply_html(f"‚ö†Ô∏è {name}, –∞–∫–∫—É—Ä–∞—Ç–Ω–µ–µ —Å –≤—ã—Ä–∞–∂–µ–Ω–∏—è–º–∏.")
                st.last_warn_at = now
            return

        if strikes < THRESHOLD:
            if now - st.last_warn_at > 15:
                await msg.reply_html(f"‚ö†Ô∏è {name}, –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ ({strikes}/{THRESHOLD}).")
                st.last_warn_at = now
            return

        try:
            me = await context.bot.get_chat_member(chat.id, context.bot.id)
            if me.can_restrict_members:
                until = datetime.now(timezone.utc) + timedelta(seconds=BAN_SECONDS)
                await context.bot.ban_chat_member(chat.id, user.id, until_date=until)
                await msg.reply_html(f"‚õî {name} –ø–æ–ª—É—á–∏–ª –±–∞–Ω –Ω–∞ {BAN_SECONDS} —Å–µ–∫—É–Ω–¥!")
            q.clear()
            st.last_warn_at = now
        except Exception as e:
            await msg.reply_html(f"‚ö†Ô∏è –û—à–∏–±–∫–∞: {e}")
